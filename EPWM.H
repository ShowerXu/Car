/*====================================================
*函数名:SET_EPWM_INIT
*功能:CCP1模块开启PWM半桥输出,占空比50%,频率为10KHz,带死区
*输入参数:无
*返回参数:无
====================================================*/
void SET_EPWM_INIT(void);
/*====================================================
*函数名:SET_EPWM_TO
*功能:改变PWM端口的占空比输出
*输入参数:per占空比(0~255)
*返回参数:无
====================================================*/
void SET_EPWM_TO(unsigned char per);
/*====================================================
*函数名:SET_EPWM_OFF
*功能:停止PWM输出
*输入参数:无
*返回参数:无
====================================================*/
void SET_EPWM_OFF(void);
/*====================================================
*函数名:SET_EPWM_ON
*功能:停止PWM输出
*输入参数:无
*返回参数:无
====================================================*/
void SET_EPWM_ON(void);
